    private void cajaEntra(javax.swing.JTextField caja, int cajaDato) {
        String textoVer = "";
        switch (cajaDato) {

            case 1:
                textoVer = "Ingrese D.N.I. sin puntos ni espacios.";
                break;
            case 2:
                textoVer = "Ingrese Nombre completo.";
                break;
            case 3:
                textoVer = "Ingrese Allidos completos.";
                break;
            case 4:
                textoVer = "Ingrese Calle, Nro. y Localidad.";
                break;
            case 5:
                textoVer = "Ingrese Telefono queda como texto.";
                break;
            case 6:
                textoVer = "Ingrese fecha formato: DD/MM/AAAA";
                break;

        }

        if (caja.getText().equals(textoVer) ) {
            caja.setText("");
            caja.setFont(f_text);
            caja.setForeground(Color.BLUE);
        }
        if (!caja.getText().equals("")){
            caja.setFont(f_text);
            caja.setForeground(Color.BLUE);
        }
        
    }

